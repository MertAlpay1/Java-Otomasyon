
package proje;


public class Gidişat {
    
    
    public String routename;
    public String[] güzergah;

    public Gidişat(String routename, String[] güzergah) {
        this.routename = routename;
        this.güzergah = güzergah;
    }

    public String getRoutename() {
        return routename;
    }

    public String[] getGüzergah() {
        return güzergah;
    }

    public void setRoutename(String routename) {
        this.routename = routename;
    }

    public void setGüzergah(String[] güzergah) {
        this.güzergah = güzergah;
    }
    
    
    
    
    
}
